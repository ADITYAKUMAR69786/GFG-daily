class Solution
{
    public:
    //Function to find the first position with different bits.
    int posOfRightMostDiffBit(int m, int n)
    {
         int c=1,p=1;
        if(m==n)return -1;
        while(true){
            bool a=m&p,b=n&p;
            if(a==b){
                c++;
                p=p<<1;
            } else return c;
        }
    }
};

